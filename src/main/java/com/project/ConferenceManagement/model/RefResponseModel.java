package com.project.ConferenceManagement.model;

public class RefResponseModel {

	private Long refId;
	private String refName;
	private String refLastName;
	
	public Long getRefId() {
		return refId;
	}
	public void setRefId(Long refId) {
		this.refId = refId;
	}
	public String getRefName() {
		return refName;
	}
	public void setRefName(String refName) {
		this.refName = refName;
	}
	public String getRefLastName() {
		return refLastName;
	}
	public void setRefLastName(String refLastName) {
		this.refLastName = refLastName;
	}
	

}
